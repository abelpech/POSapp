    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().equals("")|| txtContrasena.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Favor de Ingresar los Datos Solicitados","Aviso", JOptionPane.INFORMATION_MESSAGE);
        }  
        else{
            try{
                PreparedStatement pst = c.prepareStatement("UPDATE usuario SET nombre='"+txtNombre.getText()+"', password='"+txtContrasena.getText()+"' where nombre = '"+txtNombre.getText()+"'");
                pst.executeUpdate();

                //System.out.print("Registro modificado correctamente");
                JOptionPane.showMessageDialog(null, "Registro Modificado Correctamente","Aviso", JOptionPane.INFORMATION_MESSAGE);
                MostrarDatos();
            }
            catch(Exception e){
                System.out.print("Error al momento de modificar el registro");

            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

