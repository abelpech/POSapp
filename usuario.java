    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().equals("")|| txtContrasena.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Favor de Ingresar los Datos Solicitados","Aviso", JOptionPane.INFORMATION_MESSAGE);
        } 
        else{
        try{
            PreparedStatement pst = c.prepareStatement("INSERT INTO usuario(nombre, password) VALUES(?,?)");
            pst.setString(1,txtNombre.getText());

            pst.setString(2, txtContrasena.getText());
            pst.executeUpdate();
            System.out.println("Los datos se guardaron correctamente");


            }catch(Exception  e){

            System.out.print("No se logro guardar la informacion error en: " + e);
            }
        }
        MostrarDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

